/*
    Switch Case --> Operador de fluxo
*/

const pontuação = 50

switch (pontuação) {
    case 30:
        console.log("Rebaixado")

        break;

    case 40:
        console.log("Quase salvo")

        break;

    case 45:
        console.log("Salvo")

        break;

    default:
        console.log("Não correu risco")
        break;
}